using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Text.RegularExpressions;
using Migrator.Core;
using Migrator.Core.Models;

namespace Migrator.PlaywrightDotNet;

public class PlaywrightDotNetRenderer : IRenderer
{
    readonly string _indent;
    int _tempVarCounter = 0;
    readonly HashSet<string> _methodScopeVars = new();
    readonly Dictionary<string, string> _sourceVarMap = new();
    readonly HashSet<string> _blockedSymbols = new(StringComparer.Ordinal);
    HashSet<string> _sourceOnlyIdentifiers = new(StringComparer.Ordinal);
    HashSet<string> _setupBlockedSymbols = new(StringComparer.Ordinal);
    HashSet<string> _setupDeclaredVars = new(StringComparer.Ordinal);
    bool _useAssertionsExpect = false;

    public PlaywrightDotNetRenderer(string indent = "\t")
    {
        _indent = indent;
    }

    public string Render(TestFileModel model)
    {
        _tempVarCounter = 0;
        var sb = new StringBuilder();

        sb.AppendLine("// Generated by Migrator — Selenium C# → Playwright .NET");
        sb.AppendLine();

        var testHost = model.TestHost;
        _sourceOnlyIdentifiers = new HashSet<string>(
            model.SourceOnlyIdentifiers ?? Array.Empty<string>(),
            StringComparer.Ordinal);
        var targetNamespace = testHost?.Namespace ?? model.Namespace;
        var className = testHost?.ClassName ?? (model.ClassName + "Playwright");
        var baseClass = testHost?.BaseClass ?? "PageTest";
        _useAssertionsExpect = baseClass != "PageTest";
        var hasTestHostSetup = testHost?.SetUpStatements != null && testHost.SetUpStatements.Length > 0;

        // Usings
        if (!string.IsNullOrEmpty(targetNamespace))
        {
            // Playwright renderer always needs these core usings
            sb.AppendLine("using Microsoft.Playwright.NUnit;");
            sb.AppendLine("using NUnit.Framework;");
            sb.AppendLine("using System;");
            sb.AppendLine("using System.Threading.Tasks;");
            if (_useAssertionsExpect)
                sb.AppendLine("using Microsoft.Playwright;");
            // Additional host-specific usings from config
            if (testHost?.Usings != null && testHost.Usings.Length > 0)
            {
                var coreUsings = new HashSet<string> { "Microsoft.Playwright.NUnit", "NUnit.Framework", "System", "System.Threading.Tasks" };
                foreach (var @using in testHost.Usings)
                {
                    if (!coreUsings.Contains(@using))
                        sb.AppendLine($"using {@using};");
                }
            }
            sb.AppendLine();
            // Backward compatibility: without TestHost, use .Playwright suffix
            var nsSuffix = testHost != null ? string.Empty : ".Playwright";
            sb.AppendLine($"namespace {targetNamespace}{nsSuffix};");
            sb.AppendLine();
        }

        var hasTodoActions = model.Tests.SelectMany(t => t.BodyActions)
            .Concat(model.SetUpActions)
            .Any(a => a is UnsupportedAction or RawStatementAction or (MappedMethodInvocationAction { RequiresReview: true }));

        if (hasTodoActions)
        {
            sb.AppendLine("// WARNING: some actions need manual review. See TODO comments below.");
            sb.AppendLine();
        }

        // Class attributes
        if (testHost?.ClassAttributes != null && testHost.ClassAttributes.Length > 0)
        {
            foreach (var attr in testHost.ClassAttributes)
                sb.AppendLine($"[{attr}]");
        }

        sb.AppendLine($"public class {className} : {baseClass}");
        sb.AppendLine("{");
        sb.AppendLine();

        // Setup method — collect fixture-scoped blocked symbols for downstream test body blocking
        _setupBlockedSymbols.Clear();
        _setupDeclaredVars.Clear();
        if (hasTestHostSetup)
        {
            ResetMethodScope();
            RenderHostSetUp(sb, testHost!.SetUpStatements!, model.SetUpActions);
            sb.AppendLine();
        }
        else if (model.SetUpActions.Any())
        {
            ResetMethodScope();
            RenderSetUp(sb, model.SetUpActions);
            // After rendering setup, transfer blocked symbols to a fixture-level set
            foreach (var symbol in _blockedSymbols)
                _setupBlockedSymbols.Add(symbol);
            sb.AppendLine();
        }

        foreach (var test in model.Tests)
        {
            RenderTest(sb, test);
            sb.AppendLine();
        }

        sb.AppendLine("}");

        return sb.ToString();
    }

    void RenderHostSetUp(StringBuilder sb, string[] hostStatements, IEnumerable<TestAction> originalActions)
    {
        sb.AppendLine($"{_indent}[SetUp]");
        sb.AppendLine($"{_indent}public async Task SetUp()");
        sb.AppendLine($"{_indent}{{");

        // Render configured setup statements
        foreach (var stmt in hostStatements)
            sb.AppendLine($"{_indent}{_indent}{stmt}");

        // Preserve original mapped setup actions as comments for reference
        if (originalActions.Any())
        {
            sb.AppendLine();
            sb.AppendLine($"{_indent}{_indent}// Original Selenium setup (mapped):");
            foreach (var action in originalActions)
                RenderActionAsComment(sb, action);
        }

        sb.AppendLine($"{_indent}}}");
    }

    void RenderActionAsComment(StringBuilder sb, TestAction action)
    {
        switch (action)
        {
            case MethodInvocationAction mi:
                AppendCommentBlock(sb, _indent + _indent, mi.FullSourceText, "  ");
                break;
            case MappedMethodInvocationAction mm:
                AppendCommentBlock(sb, _indent + _indent, mm.FullSourceText, "  ");
                break;
            case ClickAction click:
                AppendCommentBlock(sb, _indent + _indent, $"click: {click.Target.SourceExpression}", "  ");
                break;
            case SendKeysAction sk:
                AppendCommentBlock(sb, _indent + _indent, $"sendKeys: {sk.Target.SourceExpression} = {sk.TextExpression}", "  ");
                break;
            case RawStatementAction raw:
                AppendCommentBlock(sb, _indent + _indent, $"raw: {raw.SourceText}", "  ");
                break;
            case UnsupportedAction unsupported:
                AppendCommentBlock(sb, _indent + _indent, $"unsupported: {unsupported.SourceText}", "  ");
                break;
            case LocalDeclarationAction lds:
                AppendCommentBlock(sb, _indent + _indent, $"var: {lds.VariableType} {lds.VariableName} = {lds.InitializationValue}", "  ");
                break;
            default:
                sb.AppendLine($"{_indent}{_indent}//   [{action.GetType().Name}]");
                break;
        }
    }

    void RenderSetUp(StringBuilder sb, IEnumerable<TestAction> actions)
    {
        sb.AppendLine($"{_indent}[SetUp]");
        sb.AppendLine($"{_indent}public async Task SetUp()");
        sb.AppendLine($"{_indent}{{");

        foreach (var action in actions)
            RenderActionWithSafety(sb, action);

        sb.AppendLine($"{_indent}}}");
    }

    void RenderTest(StringBuilder sb, TestModel test)
    {
        if (!string.IsNullOrEmpty(test.Category))
            sb.AppendLine($"{_indent}[Category(\"{test.Category}\")]");

        foreach (var caseData in test.CaseData)
        {
            if (!string.IsNullOrEmpty(caseData.RawSourceText))
            {
                var raw = NormalizeAttribute(caseData.RawSourceText);
                sb.AppendLine($"{_indent}{raw}");
            }
            else
            {
                var args = string.Join(", ", caseData.Arguments.Select(a =>
                {
                    if (a.All(char.IsDigit) || (a.Contains('.') && a.Replace(".", "").Replace("-", "").All(char.IsDigit)))
                        return a;

                    return $"\"{a}\"";
                }));
                sb.AppendLine($"{_indent}[TestCase({args})]");
            }
        }

        if (!test.CaseData.Any())
            sb.AppendLine($"{_indent}[Test]");

        var paramList = RenderParameters(test.Parameters);
        sb.AppendLine($"{_indent}public async Task {test.Name}({paramList})");
        sb.AppendLine($"{_indent}{{");
        ResetMethodScope();

        // Carry over fixture-scoped blocked symbols from setup
        foreach (var symbol in _setupBlockedSymbols)
            _blockedSymbols.Add(symbol);

        foreach (var action in test.BodyActions)
            RenderActionWithSafety(sb, action);

        sb.AppendLine($"{_indent}}}");
    }

    string RenderParameters(IEnumerable<MethodParameterModel> parameters)
    {
        if (parameters == null || !parameters.Any())
            return string.Empty;

        return string.Join(", ", parameters.Select(p =>
        {
            var param = $"{p.Type} {p.Name}";
            if (p.DefaultValue != null)
                param += $" = {p.DefaultValue}";
            return param;
        }));
    }

    string NextTempVar(string hint)
    {
        return $"{hint}_{_tempVarCounter++}";
    }

    void RenderAction(StringBuilder sb, TestAction action)
    {
        switch (action)
        {
            case ClickAction click:
                RenderClick(sb, click);
                break;
            case SendKeysAction sendKeys:
                RenderSendKeys(sb, sendKeys);
                break;
            case PressAction press:
                RenderPress(sb, press);
                break;
            case AssertThatAction assertThat:
                RenderAssertThat(sb, assertThat);
                break;
            case AssertAreEqualAction assertEqual:
                RenderAssertAreEqual(sb, assertEqual);
                break;
            case TextAssertionAction textAssert:
                RenderTextAssertion(sb, textAssert);
                break;
            case VisibilityAssertionAction visAssert:
                RenderVisibilityAssertion(sb, visAssert);
                break;
            case WaitForAction wait:
                RenderWaitFor(sb, wait);
                break;
            case UrlAssertionAction urlAssert:
                RenderUrlAssertion(sb, urlAssert);
                break;
            case MethodInvocationAction methodInv:
                RenderMethodInvocation(sb, methodInv);
                break;
            case MappedMethodInvocationAction mappedInv:
                RenderMappedMethodInvocation(sb, mappedInv);
                break;
            case UnsupportedAction unsupported:
                RenderUnsupported(sb, unsupported);
                break;
            case RawStatementAction raw:
                RenderRawStatement(sb, raw);
                break;
            case LocalDeclarationAction localDecl:
                RenderLocalDeclaration(sb, localDecl);
                break;
            case LocatorDeclarationAction locatorDecl:
                RenderLocatorDeclaration(sb, locatorDecl);
                break;
            case NavigationAction nav:
                RenderNavigation(sb, nav);
                break;
            case ConditionalBlockAction condBlock:
                RenderConditionalBlock(sb, condBlock);
                break;
            case TableRowTextAccessAction tableTextAccess:
                RenderTableRowTextAccess(sb, tableTextAccess);
                break;
            case TableCountAssertionAction tableCountAssert:
                RenderTableCountAssertion(sb, tableCountAssert);
                break;
            case TableRowAccessAction tableRowAccess:
                RenderTableRowAccess(sb, tableRowAccess);
                break;
            default:
                sb.AppendLine($"{_indent}{_indent}// [unknown action: {action.GetType().Name}]");
                break;
        }
    }

    void RenderActionWithSafety(StringBuilder sb, TestAction action)
    {
        var sourceText = GetActionSourceText(action);
        var declaredVariables = ExtractVariableNames(sourceText).ToArray();

        // For semantic actions with resolved targets (Click, SendKeys, etc.), use the rendered
        // target expression for symbol checks instead of source expression. This prevents
        // blocked setup symbols from incorrectly blocking actions that have safe Playwright locators.
        var checkText = GetCheckableSourceText(action, sourceText);

        var sourceOnlyIdentifier = FindReferencedSymbol(checkText, _sourceOnlyIdentifiers);
        if (sourceOnlyIdentifier != null)
        {
            foreach (var variable in declaredVariables)
                BlockSymbol(variable);

            RenderBlockedActionAsComment(
                sb,
                action,
                $"uses source-only identifier '{sourceOnlyIdentifier}'",
                sourceText);
            return;
        }

        var blockedSymbol = FindReferencedSymbol(checkText, _blockedSymbols);
        if (blockedSymbol != null)
        {
            foreach (var variable in declaredVariables)
                BlockSymbol(variable);

            RenderBlockedActionAsComment(
                sb,
                action,
                $"depends on unresolved symbol '{blockedSymbol}'",
                sourceText);
            return;
        }

        RenderAction(sb, action);

        if (action is RawStatementAction raw && IsResolvedRawLocatorAssignment(raw.SourceText))
            return;

        if (action is RawStatementAction or UnsupportedAction)
        {
            foreach (var variable in declaredVariables)
                BlockSymbol(variable);
        }

        // Detect references to unavailable symbols: identifiers in sourceText that are
        // not declared in current scope, not known types, and not framework built-ins.
        // Only applies to RawStatement, MethodInvocation, MappedMethodInvocation, LocalDeclaration,
        // Navigation — i.e. actions where sourceText carries real identifiers.
        // Semantic actions like ClickAction/SendKeys have synthetic sourceText and are handled
        // by their own target resolution logic.
        var unavailableRefs = FindUnavailableSymbolsForAction(action, sourceText, declaredVariables);
        if (unavailableRefs.Count > 0)
        {
            foreach (var variable in declaredVariables)
                BlockSymbol(variable);
            foreach (var refName in unavailableRefs)
                BlockSymbol(refName);

            var refList = string.Join(", ", unavailableRefs.Select(r => $"'{r}'"));
            sb.AppendLine($"{_indent}{_indent}// TODO: references unavailable symbol(s) {refList} — verify in target");
        }
    }

    /// <summary>
    /// Returns the text to use for symbol safety checks.
    /// For semantic actions with resolved targets, returns the rendered Playwright locator
    /// (which contains no source-only identifiers). For other actions, returns original sourceText.
    /// </summary>
    string GetCheckableSourceText(TestAction action, string sourceText)
    {
        return action switch
        {
            ClickAction c when c.Target.Kind != TargetKind.Unresolved => RenderTargetExpression(c.Target),
            SendKeysAction s when s.Target.Kind != TargetKind.Unresolved => RenderTargetExpression(s.Target),
            PressAction p when p.Target.Kind != TargetKind.Unresolved => RenderTargetExpression(p.Target),
            WaitForAction w when w.Target.Kind != TargetKind.Unresolved => RenderTargetExpression(w.Target),
            TextAssertionAction t when t.Target.Kind != TargetKind.Unresolved => RenderTargetExpression(t.Target),
            VisibilityAssertionAction v when v.Target.Kind != TargetKind.Unresolved => RenderTargetExpression(v.Target),
            TableCountAssertionAction t when t.Target.Kind != TargetKind.Unresolved => RenderTargetExpression(t.Target),
            TableRowAccessAction t when t.Target.Kind != TargetKind.Unresolved => RenderTargetExpression(t.Target),
            TableRowTextAccessAction t when t.Target.Kind != TargetKind.Unresolved => RenderTargetExpression(t.Target),
            _ => sourceText
        };
    }

    void RenderBlockedActionAsComment(StringBuilder sb, TestAction action, string reason, string sourceText)
    {
        sb.AppendLine($"{_indent}{_indent}// TODO: {reason}");
        AppendCommentBlock(sb, _indent + _indent, sourceText, "  ");
    }

    void RenderUnresolvedTargetComment(StringBuilder sb, TargetExpression target, string actionDescription, int sourceLine)
    {
        var escaped = EscapeComment(target.SourceExpression);
        sb.AppendLine($"{_indent}{_indent}// TODO: map source expression to Playwright locator: {escaped}");
        sb.AppendLine($"{_indent}{_indent}// {actionDescription} // line {sourceLine}");
    }

    void RenderClick(StringBuilder sb, ClickAction action)
    {
        var target = action.Target;
        if (target.Kind != TargetKind.Unresolved)
        {
            sb.AppendLine($"{_indent}{_indent}await {RenderTargetExpression(target)}.ClickAsync(); // line {action.SourceLine}");
        }
        else
        {
            RenderUnresolvedTargetComment(sb, target, "await (locator).ClickAsync()", action.SourceLine);
        }
    }

    void RenderSendKeys(StringBuilder sb, SendKeysAction action)
    {
        var target = action.Target;
        var text = ConvertExpression(action.TextExpression);
        var escaped = EscapeComment(target.SourceExpression);

        if (target.Kind != TargetKind.Unresolved)
        {
            if (string.IsNullOrWhiteSpace(text))
            {
                sb.AppendLine($"{_indent}{_indent}// TODO: source input call has no value argument: {escaped}");
                sb.AppendLine($"{_indent}{_indent}// await {RenderTargetExpression(target)}.FillAsync(/* TODO */); // line {action.SourceLine}");
            }
            else
            {
                sb.AppendLine($"{_indent}{_indent}await {RenderTargetExpression(target)}.FillAsync({text}); // line {action.SourceLine}");
            }
        }
        else
        {
            if (string.IsNullOrWhiteSpace(text))
                RenderUnresolvedTargetComment(sb, target, "await (locator).FillAsync(/* TODO */)", action.SourceLine);
            else
                RenderUnresolvedTargetComment(sb, target, $"await (locator).FillAsync({text})", action.SourceLine);
        }
    }

    void RenderPress(StringBuilder sb, PressAction action)
    {
        var target = action.Target;
        var keyName = ExtractKeyName(action.KeyName);

        if (target.Kind != TargetKind.Unresolved)
        {
            sb.AppendLine($"{_indent}{_indent}await {RenderTargetExpression(target)}.PressAsync(\"{keyName}\"); // line {action.SourceLine}");
        }
        else
        {
            RenderUnresolvedTargetComment(sb, target, $"await (locator).PressAsync(\"{keyName}\")", action.SourceLine);
        }
    }

    void RenderTextAssertion(StringBuilder sb, TextAssertionAction action)
    {
        var target = action.Target;
        var locator = target.Kind != TargetKind.Unresolved
            ? RenderTargetExpression(target)
            : "(locator)";

        if (target.Kind == TargetKind.Unresolved)
        {
            string methodLine;
            switch (action.Kind)
            {
                case TextAssertionKind.TextEquals:
                    methodLine = $"await {ExpectCall()}({locator}).ToHaveTextAsync({ConvertExpression(action.ExpectedValue!)}); // line {action.SourceLine}";
                    break;
                case TextAssertionKind.TextNotEquals:
                    methodLine = $"Assert.That(await {locator}.InnerTextAsync(), Is.Not.EqualTo({ConvertExpression(action.ExpectedValue!)})); // line {action.SourceLine}";
                    break;
                case TextAssertionKind.TextNotEmpty:
                    methodLine = $"Assert.That(await {locator}.InnerTextAsync(), Is.Not.Empty); // line {action.SourceLine}";
                    break;
                case TextAssertionKind.TextEmpty:
                    methodLine = $"Assert.That(await {locator}.InnerTextAsync(), Is.Empty); // line {action.SourceLine}";
                    break;
                case TextAssertionKind.TextContains:
                    methodLine = $"await {ExpectCall()}({locator}).ToContainTextAsync({ConvertExpression(action.ExpectedValue!)}); // line {action.SourceLine}";
                    break;
                default:
                    methodLine = $"// TODO: unsupported text assertion kind {action.Kind}";
                    break;
            }
            sb.AppendLine($"{_indent}{_indent}// {methodLine}");
            sb.AppendLine($"{_indent}{_indent}// TODO: map source expression to Playwright locator: {EscapeComment(target.SourceExpression)}");
            return;
        }

        switch (action.Kind)
        {
            case TextAssertionKind.TextEquals:
                sb.AppendLine($"{_indent}{_indent}await {ExpectCall()}({locator}).ToHaveTextAsync({ConvertExpression(action.ExpectedValue!)}); // line {action.SourceLine}");
                break;
            case TextAssertionKind.TextNotEquals:
                {
                    var nv = NextTempVar("textResult");
                    sb.AppendLine($"{_indent}{_indent}var {nv} = await {locator}.InnerTextAsync(); // line {action.SourceLine}");
                    sb.AppendLine($"{_indent}{_indent}Assert.That({nv}, Is.Not.EqualTo({ConvertExpression(action.ExpectedValue!)}));");
                    break;
                }
            case TextAssertionKind.TextNotEmpty:
                {
                    var nv2 = NextTempVar("textResult");
                    sb.AppendLine($"{_indent}{_indent}var {nv2} = await {locator}.InnerTextAsync(); // line {action.SourceLine}");
                    sb.AppendLine($"{_indent}{_indent}Assert.That({nv2}, Is.Not.Empty);");
                    break;
                }
            case TextAssertionKind.TextEmpty:
                {
                    var nv3 = NextTempVar("textResult");
                    sb.AppendLine($"{_indent}{_indent}var {nv3} = await {locator}.InnerTextAsync(); // line {action.SourceLine}");
                    sb.AppendLine($"{_indent}{_indent}Assert.That({nv3}, Is.Empty);");
                    break;
                }
            case TextAssertionKind.TextContains:
                sb.AppendLine($"{_indent}{_indent}await {ExpectCall()}({locator}).ToContainTextAsync({ConvertExpression(action.ExpectedValue!)}); // line {action.SourceLine}");
                break;
        }
    }

    void RenderVisibilityAssertion(StringBuilder sb, VisibilityAssertionAction action)
    {
        var target = action.Target;

        if (target.Kind == TargetKind.Unresolved)
        {
            var method = action.Kind == VisibilityKind.Visible ? "ToBeVisibleAsync" : "ToBeHiddenAsync";
            sb.AppendLine($"{_indent}{_indent}// await {ExpectCall()}((locator)).{method}(); // line {action.SourceLine}");
            sb.AppendLine($"{_indent}{_indent}// TODO: map source expression to Playwright locator: {EscapeComment(target.SourceExpression)}");
        }
        else
        {
            var locator = RenderTargetExpression(target);
            if (action.Kind == VisibilityKind.Visible)
            {
                sb.AppendLine($"{_indent}{_indent}await {ExpectCall()}({locator}).ToBeVisibleAsync(); // line {action.SourceLine}");
            }
            else
            {
                sb.AppendLine($"{_indent}{_indent}await {ExpectCall()}({locator}).ToBeHiddenAsync(); // line {action.SourceLine}");
            }
        }
    }

    void RenderWaitFor(StringBuilder sb, WaitForAction action)
    {
        var target = action.Target;
        if (target.Kind != TargetKind.Unresolved)
        {
            sb.AppendLine($"{_indent}{_indent}await {RenderTargetExpression(target)}.WaitForAsync(); // line {action.SourceLine}");
        }
        else
        {
            RenderUnresolvedTargetComment(sb, target, "await (locator).WaitForAsync()", action.SourceLine);
        }
    }

    void RenderUrlAssertion(StringBuilder sb, UrlAssertionAction action)
    {
        var expected = ConvertExpression(action.ExpectedValue);
        var isLiteral = expected.StartsWith("\"") && expected.EndsWith("\"");

        switch (action.Kind)
        {
            case UrlAssertionKind.UrlEquals:
                if (isLiteral)
                {
                    sb.AppendLine($"{_indent}{_indent}await {ExpectCall()}(Page).ToHaveURLAsync({expected}); // line {action.SourceLine}");
                }
                else
                {
                    sb.AppendLine($"{_indent}{_indent}// await {ExpectCall()}(Page).ToHaveURLAsync({expected}); // line {action.SourceLine}");
                    sb.AppendLine($"{_indent}{_indent}// TODO: URL assertion uses external variable — verify and uncomment");
                }
                break;
            case UrlAssertionKind.UrlContains:
                if (isLiteral)
                {
                    sb.AppendLine($"{_indent}{_indent}Assert.That(Page.Url, Does.Contain({expected})); // line {action.SourceLine}");
                }
                else
                {
                    sb.AppendLine($"{_indent}{_indent}// Assert.That(Page.Url, Does.Contain({expected})); // line {action.SourceLine}");
                    sb.AppendLine($"{_indent}{_indent}// TODO: URL assertion uses external variable — verify and uncomment");
                }
                break;
        }
    }

    void RenderMappedMethodInvocation(StringBuilder sb, MappedMethodInvocationAction action)
    {
        var processed = DeduplicateInvocationVariables(action.TargetStatements);
        for (var i = 0; i < processed.Length; i++)
        {
            var stmt = processed[i];
            var (substituted, hasUnresolved) = SubstituteTargetPlaceholder(stmt, action);

            if (_useAssertionsExpect && substituted.Contains("Expect("))
            {
                substituted = substituted.Replace("Expect(", "Assertions.Expect(");
            }

            if (hasUnresolved)
            {
                RenderMappedTargetStatementAsComment(sb, substituted, action.SourceMethod, action.SourceLine);
            }
            else
            {
                sb.AppendLine($"{_indent}{_indent}{substituted} // line {action.SourceLine}");
            }
        }
        if (action.RequiresReview)
        {
            sb.AppendLine($"{_indent}{_indent}// TODO: mapped method requires manual review — {EscapeComment(action.FullSourceText)}");
        }
    }

    /// <summary>
    /// Substitutes {TARGET} in a target statement with the resolved target expression.
    /// Also detects unknown placeholders (e.g. {UNKNOWN}) that cannot be resolved.
    /// Returns (substitutedStatement, hasUnresolvedPlaceholders).
    /// </summary>
    (string substituted, bool hasUnresolved) SubstituteTargetPlaceholder(string statement, MappedMethodInvocationAction action)
    {
        var substituted = statement;
        var hasUnresolved = false;

        // Resolve {TARGET} if target expression is available
        if (substituted.Contains("{TARGET}"))
        {
            if (action.TargetExpr != null && action.TargetExpr.Kind != TargetKind.Unresolved)
            {
                var targetExpr = RenderTargetExpression(action.TargetExpr);
                substituted = substituted.Replace("{TARGET}", targetExpr);
            }
            else
            {
                hasUnresolved = true;
            }
        }

        // Check for any remaining unknown placeholders
        if (!hasUnresolved)
        {
            var remaining = FindRemainingPlaceholders(substituted);
            if (remaining.Length > 0)
            {
                hasUnresolved = true;
            }
        }

        return (substituted, hasUnresolved);
    }

    /// <summary>
    /// Finds remaining {PLACEHOLDER} tokens in a statement after substitution.
    /// </summary>
    static string[] FindRemainingPlaceholders(string statement)
    {
        var result = new List<string>();
        var inQuote = false;
        var quoteChar = '\0';

        for (int i = 0; i < statement.Length; i++)
        {
            var c = statement[i];

            if (inQuote)
            {
                if (c == quoteChar && statement[i - 1] != '\\')
                    inQuote = false;
                continue;
            }

            if (c == '"' || c == '\'')
            {
                inQuote = true;
                quoteChar = c;
                continue;
            }

            if (c == '{' && i + 1 < statement.Length)
            {
                int start = i;
                int j = i + 1;
                while (j < statement.Length && char.IsLetterOrDigit(statement[j]))
                    j++;
                if (j < statement.Length && statement[j] == '}')
                {
                    var name = statement.Substring(start + 1, j - start - 1);
                    if (!string.IsNullOrEmpty(name))
                        result.Add("{" + name + "}");
                    i = j;
                }
            }
        }

        return result.ToArray();
    }

    /// <summary>
    /// Renders a target statement as a safe TODO comment when placeholder substitution failed.
    /// </summary>
    void RenderMappedTargetStatementAsComment(StringBuilder sb, string statement, string? sourceMethod, int sourceLine)
    {
        var methodInfo = !string.IsNullOrEmpty(sourceMethod) ? $" sourceMethod: {sourceMethod}" : string.Empty;
        sb.AppendLine($"{_indent}{_indent}// TODO: unresolved MethodMapping placeholder{methodInfo} (line {sourceLine})");
        AppendCommentBlock(sb, _indent + _indent, $"Original: {statement}", "  ");
    }

    string[] DeduplicateInvocationVariables(IReadOnlyList<string> statements)
    {
        var result = new string[statements.Count];
        var seenInInvocation = new Dictionary<string, string>();

        for (var i = 0; i < statements.Count; i++)
        {
            var stmt = statements[i].Trim();
            if (stmt.StartsWith("var "))
            {
                var eqIdx = stmt.IndexOf('=');
                if (eqIdx > 0)
                {
                    var varName = stmt.Substring(4, eqIdx - 4).Trim();
                    string finalName;

                    if (_methodScopeVars.Contains(varName))
                    {
                        var newVar = NextTempVar(varName);
                        while (_methodScopeVars.Contains(newVar))
                            newVar = NextTempVar(varName);
                        finalName = newVar;
                        _methodScopeVars.Add(finalName);
                    }
                    else
                    {
                        finalName = varName;
                        _methodScopeVars.Add(varName);
                    }

                    seenInInvocation[varName] = finalName;
                    result[i] = $"var {finalName}{stmt.Substring(eqIdx)}";
                    continue;
                }
            }

            var resolved = ProtectPlaceholders(stmt, out var placeholders);
            foreach (var (orig, renamed) in seenInInvocation)
                resolved = Regex.Replace(resolved, $@"\b{Regex.Escape(orig)}\b", renamed);
            result[i] = RestorePlaceholders(resolved, placeholders);
        }

        return result;
    }

    /// <summary>
    /// Temporarily replaces {PLACEHOLDER} tokens with unique markers to prevent
    /// DeduplicateInvocationVariables from treating placeholder names as variable references.
    /// </summary>
    static string ProtectPlaceholders(string stmt, out string[] placeholders)
    {
        var matches = Regex.Matches(stmt, @"\{(\w+)\}");
        placeholders = new string[matches.Count];
        var protectedStmt = stmt;
        for (int i = 0; i < matches.Count; i++)
        {
            var ph = matches[i].Value;
            placeholders[i] = ph;
            protectedStmt = protectedStmt.Replace(ph, $"__PH_{i}__");
        }
        return protectedStmt;
    }

    /// <summary>
    /// Restores placeholder tokens after deduplication.
    /// </summary>
    static string RestorePlaceholders(string stmt, string[] placeholders)
    {
        for (int i = 0; i < placeholders.Length; i++)
        {
            stmt = stmt.Replace($"__PH_{i}__", placeholders[i]);
        }
        return stmt;
    }

