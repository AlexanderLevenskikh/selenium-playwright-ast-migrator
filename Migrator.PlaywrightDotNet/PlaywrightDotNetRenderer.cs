        sb.AppendLine($"{_indent}{_indent}var {variableName} = {_pageVariable}; // MIGRATOR: compile-only navigation page variable fallback [MIGRATOR:NAVIGATION_FALLBACK_DECLARATION]");
        RegisterTargetLocal(variableName);
        RegisterSourceVar(variableName, variableName);
    }

    static string EnsureStatementTerminated(string statement)
    {
        return statement.EndsWith(";", StringComparison.Ordinal) ? statement : statement + ";";
    }

    void RenderConditionalBlock(StringBuilder sb, ConditionalBlockAction action)
    {
        // If all branches are empty (e.g. body was fully suppressed), render condition as
        // a preserved comment with the suppressed body note, regardless of unresolved condition
        // symbols. The condition won't execute anyway since the body is suppressed.
        var allEmptyOrSafelySuppressed = ConditionalBranchesEmptyOrSafelySuppressed(action);
        if (allEmptyOrSafelySuppressed)
        {
            AppendSmartTodo(
                sb,
                $"conditional block with suppressed body: {EscapeComment(action.ConditionExpression)}",
                "CONDITIONAL_SUPPRESSED_BODY",
                "All actions inside the conditional block were suppressed by adapter config. The condition expression is preserved for context.",
                "Add adapter mappings for suppressed body actions if the translation is deterministic.");
            AppendCommentLine(sb, _indent + _indent, $"if ({action.ConditionExpression}) {{ /* body suppressed */ }}");
            return;
        }

        var unresolvedConditions = FindUnresolvedConditionalSymbols(action).ToArray();
        if (unresolvedConditions.Length > 0)
        {
            AppendSmartTodo(
                sb,
                $"conditional block depends on unresolved condition symbol(s): {string.Join(", ", unresolvedConditions.Select(s => $"'{s}'"))}",
                "CONDITIONAL_UNRESOLVED_SYMBOL",
                "The condition references a source variable that was not generated in the target method.",
                "Map or stub the declaration first; otherwise keep the whole conditional block for manual migration.");
            AppendCommentBlock(sb, _indent + _indent, RenderConditionalSourceSummary(action), "  ");
            foreach (var symbol in unresolvedConditions)
                BlockSymbol(symbol);
            return;
        }

        sb.AppendLine($"{_indent}{_indent}if ({action.ConditionExpression})");
        sb.AppendLine($"{_indent}{_indent}{{");
        foreach (var a in action.IfActions)
            RenderActionWithSafety(sb, a);

        foreach (var elseIf in action.ElseIfActions)
        {
            sb.AppendLine($"{_indent}{_indent}}} else if ({elseIf.Condition})");
            sb.AppendLine($"{_indent}{_indent}{{");
            foreach (var a in elseIf.Actions)
                RenderActionWithSafety(sb, a);
        }

        if (action.ElseActions.Any())
        {
            sb.AppendLine($"{_indent}{_indent}}} else");
            sb.AppendLine($"{_indent}{_indent}{{");
            foreach (var a in action.ElseActions)
                RenderActionWithSafety(sb, a);
        }

        sb.AppendLine($"{_indent}{_indent}}}");
    }

    bool ConditionalBranchesEmptyOrSafelySuppressed(ConditionalBlockAction action)
    {
        return BranchEmptyOrSafelySuppressed(action.IfActions)
            && action.ElseIfActions.All(e => BranchEmptyOrSafelySuppressed(e.Actions))
            && BranchEmptyOrSafelySuppressed(action.ElseActions);
    }

    bool BranchEmptyOrSafelySuppressed(IReadOnlyList<TestAction> actions)
    {
        if (actions.Count == 0)
            return true;

        return actions.All(IsSafelySuppressibleConditionalBodyAction);
    }

    bool IsSafelySuppressibleConditionalBodyAction(TestAction action)
    {
        var sourceText = GetActionSourceText(action);
        return IsSuppressedAction(action, sourceText) && !IsAssertionLikeSource(sourceText);
    }

    IEnumerable<string> FindUnresolvedConditionalSymbols(ConditionalBlockAction action)
    {
        foreach (var symbol in FindUnavailableSymbols(action.ConditionExpression, Array.Empty<string>()))
            yield return symbol;

        foreach (var elseIf in action.ElseIfActions)
        {
            foreach (var symbol in FindUnavailableSymbols(elseIf.Condition, Array.Empty<string>()))
                yield return symbol;
        }
    }

    static string RenderConditionalSourceSummary(ConditionalBlockAction action)
    {
        var sb = new StringBuilder();
        sb.Append("if (").Append(action.ConditionExpression).AppendLine(") { ... }");
        foreach (var elseIf in action.ElseIfActions)
            sb.Append("else if (").Append(elseIf.Condition).AppendLine(") { ... }");
        if (action.ElseActions.Any())
            sb.AppendLine("else { ... }");
        return sb.ToString().TrimEnd();
    }

    string ExtractKeyName(string keyExpression)
    {
        var expr = keyExpression.Trim();
        if (expr.StartsWith("Keys.", System.StringComparison.Ordinal))
            return expr.Substring("Keys.".Length);
        if (expr.StartsWith('"') && expr.EndsWith('"'))
            return expr.Substring(1, expr.Length - 2);
        return expr;
    }

    string NormalizeAttribute(string rawAttribute)
    {
        var raw = rawAttribute.Trim();
        if (raw.StartsWith("[") && raw.EndsWith("]"))
            return raw;

        if (raw.StartsWith("["))
            return raw + "]";

        return $"[{raw}]";
    }

    string RenderTargetExpression(TargetExpression target)
    {
        if (target is MappedTarget mapped && mapped.TestIdAttribute != null && target.Kind == TargetKind.PlaywrightLocator)
        {
            var attr = EscapeAttribute(mapped.TestIdAttribute);
            var value = EscapeString(ExtractTestIdValue(mapped.TargetExpression));
            var expr = $"{_pageVariable}.Locator(\"[{attr}='{value}']\")";
            return ApplyMatchStrategy(expr, mapped);
        }

        var rendered = target.RenderLocator();
        return target.Kind switch
        {
            TargetKind.PlaywrightLocator => RenderPlaywrightLocator(mapped: target as MappedTarget, rendered),
            TargetKind.CssSelector => RenderCssSelectorLocator(mapped: target as MappedTarget),
            TargetKind.TestIdBeginning => RenderTestIdBeginningLocator(mapped: target as MappedTarget),
            TargetKind.ClassNameBeginning => RenderClassNameBeginningLocator(mapped: target as MappedTarget),
            TargetKind.Text => RenderTextLocator(mapped: target as MappedTarget),
            TargetKind.PageObjectProperty => rendered,
            TargetKind.RawExpression => ApplyMatchStrategy(rendered, target as MappedTarget),
            _ => $"{_pageVariable}.Locator(\"TODO: {target.SourceExpression}\")"
        };
    }

    string RenderCssSelectorLocator(MappedTarget? mapped)
    {
        var selector = EscapeStringLiteral(mapped?.TargetExpression ?? "TODO");
        return ApplyMatchStrategy($"{_pageVariable}.Locator(\"{selector}\")", mapped);
    }

    string RenderTestIdBeginningLocator(MappedTarget? mapped)
    {
        var attr = EscapeAttribute(mapped?.TestIdAttribute ?? "data-testid");
        var prefix = EscapeAttributeValue(mapped?.TargetExpression ?? "TODO");
        return ApplyMatchStrategy($"{_pageVariable}.Locator(\"[{attr}^='{prefix}']\")", mapped);
    }

    string RenderClassNameBeginningLocator(MappedTarget? mapped)
    {
        var prefix = EscapeAttributeValue(mapped?.TargetExpression ?? "TODO");
        return ApplyMatchStrategy($"{_pageVariable}.Locator(\"[class^='{prefix}']\")", mapped);
    }

    string RenderPlaywrightLocator(MappedTarget? mapped, string rendered)
    {
        // Target config may already contain a fully-qualified Playwright expression,
        // historically written with Page.*. Normalize that root to the configured
        // target page variable so scoped projects can use lowercase `page` without
        // producing CS0117 against the Page type.
        if (TryNormalizePlaywrightPageRoot(rendered, out var normalizedRendered))
            return ApplyMatchStrategy(normalizedRendered, mapped);

        // Legacy: TargetExpression already contains Playwright call like GetByTestId("x")
        if (IsLegacyPlaywrightFragment(rendered))
        {
            var expr = $"{_pageVariable}.{rendered}";
            return ApplyMatchStrategy(expr, mapped);
        }

        // Semantic: TargetExpression is a raw test-id value like "submit"
        if (mapped != null)
        {
            var expr = $"{_pageVariable}.GetByTestId(\"{ExtractTestIdValue(mapped.TargetExpression)}\")";
            return ApplyMatchStrategy(expr, mapped);
        }

        return $"{_pageVariable}.{rendered}";
    }

    bool TryNormalizePlaywrightPageRoot(string rendered, out string normalized)
    {
        normalized = rendered;

        if (rendered.StartsWith($"{_pageVariable}.", StringComparison.Ordinal))
            return true;

        if (TryReplacePlaywrightPageRoot(rendered, "Page", out normalized))
            return true;

        if (TryReplacePlaywrightPageRoot(rendered, "page", out normalized))
            return true;

        return false;
    }

    bool TryReplacePlaywrightPageRoot(string expression, string root, out string normalized)
    {
        normalized = expression;
        var prefix = root + ".";
        if (!expression.StartsWith(prefix, StringComparison.Ordinal))
            return false;

        normalized = _pageVariable + expression.Substring(root.Length);
        return true;
    }

    string RenderTextLocator(MappedTarget? mapped)
    {
        if (mapped == null)
            return $"{_pageVariable}.GetByText(\"TODO\")";

        var expr = $"{_pageVariable}.GetByText(\"{EscapeString(mapped.TargetExpression)}\")";
        return ApplyMatchStrategy(expr, mapped);
    }

    string ApplyMatchStrategy(string locatorExpr, MappedTarget? mapped)
    {
        if (mapped == null || string.IsNullOrEmpty(mapped.Match))
            return locatorExpr;

        return mapped.Match switch
        {
            "First" => $"{locatorExpr}.First",
            "Nth" when mapped.NthIndex.HasValue => $"{locatorExpr}.Nth({mapped.NthIndex.Value})",
            "Nth" when IsSafeIndexExpression(mapped.NthIndexExpression) => $"{locatorExpr}.Nth({mapped.NthIndexExpression})",
            "Nth" => locatorExpr,
            _ => locatorExpr
        };
    }

