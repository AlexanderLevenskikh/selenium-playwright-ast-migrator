using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Text.RegularExpressions;
using Migrator.Core;
using Migrator.Core.Models;

namespace Migrator.PlaywrightDotNet;

public class PlaywrightDotNetRenderer : IRenderer
{
    readonly string _indent;
    int _tempVarCounter = 0;
    readonly HashSet<string> _methodScopeVars = new();
    readonly Dictionary<string, string> _sourceVarMap = new();
    bool _useAssertionsExpect = false;

    public PlaywrightDotNetRenderer(string indent = "\t")
    {
        _indent = indent;
    }

    public string Render(TestFileModel model)
    {
        _tempVarCounter = 0;
        var sb = new StringBuilder();

        sb.AppendLine("// Generated by Migrator — Selenium C# → Playwright .NET");
        sb.AppendLine();

        var testHost = model.TestHost;
        var targetNamespace = testHost?.Namespace ?? model.Namespace;
        var className = testHost?.ClassName ?? (model.ClassName + "Playwright");
        var baseClass = testHost?.BaseClass ?? "PageTest";
        _useAssertionsExpect = baseClass != "PageTest";
        var hasTestHostSetup = testHost?.SetUpStatements != null && testHost.SetUpStatements.Length > 0;

        // Usings
        if (!string.IsNullOrEmpty(targetNamespace))
        {
            // Playwright renderer always needs these core usings
            sb.AppendLine("using Microsoft.Playwright.NUnit;");
            sb.AppendLine("using NUnit.Framework;");
            sb.AppendLine("using System.Threading.Tasks;");
            if (_useAssertionsExpect)
                sb.AppendLine("using Microsoft.Playwright;");
            // Additional host-specific usings from config
            if (testHost?.Usings != null && testHost.Usings.Length > 0)
            {
                var coreUsings = new HashSet<string> { "Microsoft.Playwright.NUnit", "NUnit.Framework", "System.Threading.Tasks" };
                foreach (var @using in testHost.Usings)
                {
                    if (!coreUsings.Contains(@using))
                        sb.AppendLine($"using {@using};");
                }
            }
            sb.AppendLine();
            // Backward compatibility: without TestHost, use .Playwright suffix
            var nsSuffix = testHost != null ? string.Empty : ".Playwright";
            sb.AppendLine($"namespace {targetNamespace}{nsSuffix};");
            sb.AppendLine();
        }

        var hasTodoActions = model.Tests.SelectMany(t => t.BodyActions)
            .Concat(model.SetUpActions)
            .Any(a => a is UnsupportedAction or RawStatementAction or (MappedMethodInvocationAction { RequiresReview: true }));

        if (hasTodoActions)
        {
            sb.AppendLine("// WARNING: some actions need manual review. See TODO comments below.");
            sb.AppendLine();
        }

        // Class attributes
        if (testHost?.ClassAttributes != null && testHost.ClassAttributes.Length > 0)
        {
            foreach (var attr in testHost.ClassAttributes)
                sb.AppendLine($"[{attr}]");
        }

        sb.AppendLine($"public class {className} : {baseClass}");
        sb.AppendLine("{");
        sb.AppendLine();

        // Setup method
        if (hasTestHostSetup)
        {
            ResetMethodScope();
            RenderHostSetUp(sb, testHost!.SetUpStatements!, model.SetUpActions);
            sb.AppendLine();
        }
        else if (model.SetUpActions.Any())
        {
            ResetMethodScope();
            RenderSetUp(sb, model.SetUpActions);
            sb.AppendLine();
        }

        foreach (var test in model.Tests)
        {
            RenderTest(sb, test);
            sb.AppendLine();
        }

        sb.AppendLine("}");

        return sb.ToString();
    }

    void RenderHostSetUp(StringBuilder sb, string[] hostStatements, IEnumerable<TestAction> originalActions)
    {
        sb.AppendLine($"{_indent}[SetUp]");
        sb.AppendLine($"{_indent}public async Task SetUp()");
        sb.AppendLine($"{_indent}{{");

        // Render configured setup statements
        foreach (var stmt in hostStatements)
            sb.AppendLine($"{_indent}{_indent}{stmt}");

        // Preserve original mapped setup actions as comments for reference
        if (originalActions.Any())
        {
            sb.AppendLine();
            sb.AppendLine($"{_indent}{_indent}// Original Selenium setup (mapped):");
            foreach (var action in originalActions)
                RenderActionAsComment(sb, action);
        }

        sb.AppendLine($"{_indent}}}");
    }

    void RenderActionAsComment(StringBuilder sb, TestAction action)
    {
        switch (action)
        {
            case MethodInvocationAction mi:
                sb.AppendLine($"{_indent}{_indent}//   {EscapeComment(mi.FullSourceText)}");
                break;
            case MappedMethodInvocationAction mm:
                sb.AppendLine($"{_indent}{_indent}//   {EscapeComment(mm.FullSourceText)}");
                break;
            case ClickAction click:
                sb.AppendLine($"{_indent}{_indent}//   click: {EscapeComment(click.Target.SourceExpression)}");
                break;
            case SendKeysAction sk:
                sb.AppendLine($"{_indent}{_indent}//   sendKeys: {EscapeComment(sk.Target.SourceExpression)} = {EscapeComment(sk.TextExpression)}");
                break;
            case RawStatementAction raw:
                sb.AppendLine($"{_indent}{_indent}//   raw: {EscapeComment(raw.SourceText)}");
                break;
            case UnsupportedAction unsupported:
                sb.AppendLine($"{_indent}{_indent}//   unsupported: {EscapeComment(unsupported.SourceText)}");
                break;
            case LocalDeclarationAction lds:
                sb.AppendLine($"{_indent}{_indent}//   var: {EscapeComment(lds.VariableType)} {EscapeComment(lds.VariableName)} = {EscapeComment(lds.InitializationValue)}");
                break;
            default:
                sb.AppendLine($"{_indent}{_indent}//   [{action.GetType().Name}]");
                break;
        }
    }

    void RenderSetUp(StringBuilder sb, IEnumerable<TestAction> actions)
    {
        sb.AppendLine($"{_indent}[SetUp]");
        sb.AppendLine($"{_indent}public async Task SetUp()");
        sb.AppendLine($"{_indent}{{");

        foreach (var action in actions)
            RenderAction(sb, action);

        sb.AppendLine($"{_indent}}}");
    }

    void RenderTest(StringBuilder sb, TestModel test)
    {
        if (!string.IsNullOrEmpty(test.Category))
            sb.AppendLine($"{_indent}[Category(\"{test.Category}\")]");

        foreach (var caseData in test.CaseData)
        {
            if (!string.IsNullOrEmpty(caseData.RawSourceText))
            {
                var raw = NormalizeAttribute(caseData.RawSourceText);
                sb.AppendLine($"{_indent}{raw}");
            }
            else
            {
                var args = string.Join(", ", caseData.Arguments.Select(a =>
                {
                    if (a.All(char.IsDigit) || (a.Contains('.') && a.Replace(".", "").Replace("-", "").All(char.IsDigit)))
                        return a;

                    return $"\"{a}\"";
                }));
                sb.AppendLine($"{_indent}[TestCase({args})]");
            }
        }

        if (!test.CaseData.Any())
            sb.AppendLine($"{_indent}[Test]");

        var paramList = RenderParameters(test.Parameters);
        sb.AppendLine($"{_indent}public async Task {test.Name}({paramList})");
        sb.AppendLine($"{_indent}{{");
        ResetMethodScope();

        foreach (var action in test.BodyActions)
            RenderAction(sb, action);

        sb.AppendLine($"{_indent}}}");
    }

    string RenderParameters(IEnumerable<MethodParameterModel> parameters)
    {
        if (parameters == null || !parameters.Any())
            return string.Empty;

        return string.Join(", ", parameters.Select(p =>
        {
            var param = $"{p.Type} {p.Name}";
            if (p.DefaultValue != null)
                param += $" = {p.DefaultValue}";
            return param;
        }));
    }

    string NextTempVar(string hint)
    {
        return $"{hint}_{_tempVarCounter++}";
    }

    void RenderAction(StringBuilder sb, TestAction action)
    {
        switch (action)
        {
            case ClickAction click:
                RenderClick(sb, click);
                break;
            case SendKeysAction sendKeys:
                RenderSendKeys(sb, sendKeys);
                break;
            case PressAction press:
                RenderPress(sb, press);
                break;
            case AssertThatAction assertThat:
                RenderAssertThat(sb, assertThat);
                break;
            case AssertAreEqualAction assertEqual:
                RenderAssertAreEqual(sb, assertEqual);
                break;
            case TextAssertionAction textAssert:
                RenderTextAssertion(sb, textAssert);
                break;
            case VisibilityAssertionAction visAssert:
                RenderVisibilityAssertion(sb, visAssert);
                break;
            case WaitForAction wait:
                RenderWaitFor(sb, wait);
                break;
            case UrlAssertionAction urlAssert:
                RenderUrlAssertion(sb, urlAssert);
                break;
            case MethodInvocationAction methodInv:
                RenderMethodInvocation(sb, methodInv);
                break;
            case MappedMethodInvocationAction mappedInv:
                RenderMappedMethodInvocation(sb, mappedInv);
                break;
            case UnsupportedAction unsupported:
                RenderUnsupported(sb, unsupported);
                break;
            case RawStatementAction raw:
                RenderRawStatement(sb, raw);
                break;
            case LocalDeclarationAction localDecl:
                RenderLocalDeclaration(sb, localDecl);
                break;
            case TableRowTextAccessAction tableTextAccess:
                RenderTableRowTextAccess(sb, tableTextAccess);
                break;
            case TableCountAssertionAction tableCountAssert:
                RenderTableCountAssertion(sb, tableCountAssert);
                break;
            case TableRowAccessAction tableRowAccess:
                RenderTableRowAccess(sb, tableRowAccess);
                break;
            default:
                sb.AppendLine($"{_indent}{_indent}// [unknown action: {action.GetType().Name}]");
                break;
        }
    }

    void RenderClick(StringBuilder sb, ClickAction action)
    {
        var target = action.Target;
        if (target.Kind != TargetKind.Unresolved)
        {
            sb.AppendLine($"{_indent}{_indent}await {RenderTargetExpression(target)}.ClickAsync(); // line {action.SourceLine}");
        }
        else
        {
            var escaped = EscapeComment(target.SourceExpression);
            var locator = EscapeStringLiteral(target.SourceExpression);
            sb.AppendLine($"{_indent}{_indent}// TODO: map source expression to Playwright locator: {escaped}");
            sb.AppendLine($"{_indent}{_indent}await Page.Locator(\"TODO: {locator}\").ClickAsync(); // line {action.SourceLine}");
        }
    }

    void RenderSendKeys(StringBuilder sb, SendKeysAction action)
    {
        var target = action.Target;
        var text = ConvertExpression(action.TextExpression);
        var escaped = EscapeComment(target.SourceExpression);
        var locator = EscapeStringLiteral(target.SourceExpression);

        if (target.Kind != TargetKind.Unresolved)
        {
            if (string.IsNullOrWhiteSpace(text))
            {
                sb.AppendLine($"{_indent}{_indent}// TODO: source input call has no value argument: {escaped}");
                sb.AppendLine($"{_indent}{_indent}// await {RenderTargetExpression(target)}.FillAsync(/* TODO */); // line {action.SourceLine}");
            }
            else
            {
                sb.AppendLine($"{_indent}{_indent}await {RenderTargetExpression(target)}.FillAsync({text}); // line {action.SourceLine}");
            }
        }
        else
        {
            sb.AppendLine($"{_indent}{_indent}// TODO: map source expression to Playwright locator: {escaped}");
            if (string.IsNullOrWhiteSpace(text))
                sb.AppendLine($"{_indent}{_indent}// await Page.Locator(\"TODO: {locator}\").FillAsync(/* TODO */); // line {action.SourceLine}");
            else
                sb.AppendLine($"{_indent}{_indent}await Page.Locator(\"TODO: {locator}\").FillAsync({text}); // line {action.SourceLine}");
        }
    }

    void RenderPress(StringBuilder sb, PressAction action)
    {
        var target = action.Target;
        var keyName = ExtractKeyName(action.KeyName);
        var escaped = EscapeComment(target.SourceExpression);
        var locator = EscapeStringLiteral(target.SourceExpression);

        if (target.Kind != TargetKind.Unresolved)
        {
            sb.AppendLine($"{_indent}{_indent}await {RenderTargetExpression(target)}.PressAsync(\"{keyName}\"); // line {action.SourceLine}");
        }
        else
        {
            sb.AppendLine($"{_indent}{_indent}// TODO: map source expression to Playwright locator: {escaped}");
            sb.AppendLine($"{_indent}{_indent}await Page.Locator(\"TODO: {locator}\").PressAsync(\"{keyName}\"); // line {action.SourceLine}");
        }
    }

    void RenderTextAssertion(StringBuilder sb, TextAssertionAction action)
    {
        var target = action.Target;
        var locator = target.Kind != TargetKind.Unresolved
            ? RenderTargetExpression(target)
            : $"Page.Locator(\"TODO: {EscapeStringLiteral(target.SourceExpression)}\")";

        switch (action.Kind)
        {
            case TextAssertionKind.TextEquals:
                sb.AppendLine($"{_indent}{_indent}await {ExpectCall()}({locator}).ToHaveTextAsync({ConvertExpression(action.ExpectedValue!)}); // line {action.SourceLine}");
                break;
            case TextAssertionKind.TextNotEquals:
                var nv = NextTempVar("textResult");
                sb.AppendLine($"{_indent}{_indent}var {nv} = await {locator}.InnerTextAsync(); // line {action.SourceLine}");
                sb.AppendLine($"{_indent}{_indent}Assert.That({nv}, Is.Not.EqualTo({ConvertExpression(action.ExpectedValue!)}));");
                break;
            case TextAssertionKind.TextNotEmpty:
                var nv2 = NextTempVar("textResult");
                sb.AppendLine($"{_indent}{_indent}var {nv2} = await {locator}.InnerTextAsync(); // line {action.SourceLine}");
                sb.AppendLine($"{_indent}{_indent}Assert.That({nv2}, Is.Not.Empty);");
                break;
            case TextAssertionKind.TextEmpty:
                var nv3 = NextTempVar("textResult");
                sb.AppendLine($"{_indent}{_indent}var {nv3} = await {locator}.InnerTextAsync(); // line {action.SourceLine}");
                sb.AppendLine($"{_indent}{_indent}Assert.That({nv3}, Is.Empty);");
                break;
            case TextAssertionKind.TextContains:
                sb.AppendLine($"{_indent}{_indent}await {ExpectCall()}({locator}).ToContainTextAsync({ConvertExpression(action.ExpectedValue!)}); // line {action.SourceLine}");
                break;
        }

        if (target.Kind == TargetKind.Unresolved)
        {
            sb.AppendLine($"{_indent}{_indent}// TODO: map source expression to Playwright locator: {EscapeComment(target.SourceExpression)}");
        }
    }

    void RenderVisibilityAssertion(StringBuilder sb, VisibilityAssertionAction action)
    {
        var target = action.Target;
        var locator = target.Kind != TargetKind.Unresolved
            ? RenderTargetExpression(target)
            : $"Page.Locator(\"TODO: {EscapeStringLiteral(target.SourceExpression)}\")";

        if (action.Kind == VisibilityKind.Visible)
        {
            sb.AppendLine($"{_indent}{_indent}await {ExpectCall()}({locator}).ToBeVisibleAsync(); // line {action.SourceLine}");
        }
        else
        {
            sb.AppendLine($"{_indent}{_indent}await {ExpectCall()}({locator}).ToBeHiddenAsync(); // line {action.SourceLine}");
        }

        if (target.Kind == TargetKind.Unresolved)
        {
            sb.AppendLine($"{_indent}{_indent}// TODO: map source expression to Playwright locator: {EscapeComment(target.SourceExpression)}");
        }
    }

    void RenderWaitFor(StringBuilder sb, WaitForAction action)
    {
        var target = action.Target;
        var escaped = EscapeComment(target.SourceExpression);
        var locator = EscapeStringLiteral(target.SourceExpression);
        if (target.Kind != TargetKind.Unresolved)
        {
            sb.AppendLine($"{_indent}{_indent}await {RenderTargetExpression(target)}.WaitForAsync(); // line {action.SourceLine}");
        }
        else
        {
            sb.AppendLine($"{_indent}{_indent}// TODO: map source expression to Playwright locator: {escaped}");
            sb.AppendLine($"{_indent}{_indent}await Page.Locator(\"TODO: {locator}\").WaitForAsync(); // line {action.SourceLine}");
        }
    }

    void RenderUrlAssertion(StringBuilder sb, UrlAssertionAction action)
    {
        var expected = ConvertExpression(action.ExpectedValue);
        var isLiteral = expected.StartsWith("\"") && expected.EndsWith("\"");

        switch (action.Kind)
        {
            case UrlAssertionKind.UrlEquals:
                if (isLiteral)
                {
                    sb.AppendLine($"{_indent}{_indent}await {ExpectCall()}(Page).ToHaveURLAsync({expected}); // line {action.SourceLine}");
                }
                else
                {
                    sb.AppendLine($"{_indent}{_indent}// await {ExpectCall()}(Page).ToHaveURLAsync({expected}); // line {action.SourceLine}");
                    sb.AppendLine($"{_indent}{_indent}// TODO: URL assertion uses external variable — verify and uncomment");
                }
                break;
            case UrlAssertionKind.UrlContains:
                if (isLiteral)
                {
                    sb.AppendLine($"{_indent}{_indent}Assert.That(Page.Url, Does.Contain({expected})); // line {action.SourceLine}");
                }
                else
                {
                    sb.AppendLine($"{_indent}{_indent}// Assert.That(Page.Url, Does.Contain({expected})); // line {action.SourceLine}");
                    sb.AppendLine($"{_indent}{_indent}// TODO: URL assertion uses external variable — verify and uncomment");
                }
                break;
        }
    }

    void RenderMappedMethodInvocation(StringBuilder sb, MappedMethodInvocationAction action)
    {
        var processed = DeduplicateInvocationVariables(action.TargetStatements);
        for (var i = 0; i < processed.Length; i++)
        {
            var stmt = processed[i];
            if (_useAssertionsExpect && stmt.Contains("Expect("))
            {
                stmt = stmt.Replace("Expect(", "Assertions.Expect(");
            }
            sb.AppendLine($"{_indent}{_indent}{stmt} // line {action.SourceLine}");
        }
        if (action.RequiresReview)
        {
            sb.AppendLine($"{_indent}{_indent}// TODO: mapped method requires manual review — {EscapeComment(action.FullSourceText)}");
        }
    }

    string[] DeduplicateInvocationVariables(IReadOnlyList<string> statements)
    {
        var result = new string[statements.Count];
        var seenInInvocation = new Dictionary<string, string>();

        for (var i = 0; i < statements.Count; i++)
        {
            var stmt = statements[i].Trim();
            if (stmt.StartsWith("var "))
            {
                var eqIdx = stmt.IndexOf('=');
                if (eqIdx > 0)
                {
                    var varName = stmt.Substring(4, eqIdx - 4).Trim();
                    string finalName;

                    if (_methodScopeVars.Contains(varName))
                    {
                        var newVar = NextTempVar(varName);
                        while (_methodScopeVars.Contains(newVar))
                            newVar = NextTempVar(varName);
                        finalName = newVar;
                        _methodScopeVars.Add(finalName);
                    }
                    else
                    {
                        finalName = varName;
                        _methodScopeVars.Add(varName);
                    }

                    seenInInvocation[varName] = finalName;
                    result[i] = $"var {finalName}{stmt.Substring(eqIdx)}";
                    continue;
                }
            }

            var resolved = stmt;
            foreach (var (orig, renamed) in seenInInvocation)
                resolved = Regex.Replace(resolved, $@"\b{Regex.Escape(orig)}\b", renamed);
            result[i] = resolved;
        }

        return result;
    }

    void RenderAssertThat(StringBuilder sb, AssertThatAction action)
    {
        var actual = ConvertExpression(action.ActualExpression);
        var constraint = ConvertConstraint(action.ConstraintExpression);
        sb.AppendLine($"{_indent}{_indent}// Assert.That({actual}, {constraint}); // line {action.SourceLine}");
        sb.AppendLine($"{_indent}{_indent}// TODO: convert constraint to Playwright assertion");
    }

    void RenderAssertAreEqual(StringBuilder sb, AssertAreEqualAction action)
    {
        var expected = ConvertExpression(action.ExpectedExpression);
        var actual = ConvertExpression(action.ActualExpression);
        sb.AppendLine(
            $"{_indent}{_indent}Assert.That({actual}, Is.EqualTo({expected})); // line {action.SourceLine}");
    }

    void RenderMethodInvocation(StringBuilder sb, MethodInvocationAction action)
    {
        var escaped = EscapeComment(action.FullSourceText);

        sb.AppendLine($"{_indent}{_indent}// [{action.MethodName}] {escaped} // line {action.SourceLine}");
        if (!IsLowPriorityMethod(action.MethodName, action.FullSourceText))
        {
            sb.AppendLine($"{_indent}{_indent}// TODO: manual review needed");
        }
    }

    void RenderUnsupported(StringBuilder sb, UnsupportedAction action)
    {
        sb.AppendLine($"{_indent}{_indent}// TODO: UNSUPPORTED [{action.Reason}]");
        sb.AppendLine($"{_indent}{_indent}//   {EscapeComment(action.SourceText)}");
    }

    void RenderRawStatement(StringBuilder sb, RawStatementAction action)
    {
        if (IsTrivialRawStatement(action.SourceText))
        {
            sb.AppendLine($"{_indent}{_indent}// source: {EscapeComment(action.SourceText)} // line {action.SourceLine}");
        }
        else
        {
            sb.AppendLine($"{_indent}{_indent}// TODO: raw statement — review: {EscapeComment(action.SourceText)}");
        }
    }

    /// <summary>
    /// Returns true for methods proven to have no runtime side effects.
    /// Currently conservative — no methods are suppressed globally.
    /// Use adapter config MethodMapping instead of adding entries here.
    /// </summary>
    static bool IsLowPriorityMethod(string methodName, string? fullSourceText)
    {
        // Reject patterns that always have side effects — never suppress.
        if (methodName.StartsWith("ClickAndOpen", StringComparison.Ordinal))
            return false;
        if (methodName.StartsWith("Open", StringComparison.Ordinal))
            return false;
        if (fullSourceText is not null && fullSourceText.Contains("Navigation.", StringComparison.Ordinal))
            return false;

        // No methods currently suppressed globally.
        // Methods like ValidateLoading, ExecuteScript, Window, SettingPeriod may have side effects
        // and should be handled via adapter config MethodMapping per-project.
        return false;
    }

    /// <summary>
    /// Returns true for raw statements that are provably trivial (no variable capture, no side effects).
    /// Does NOT suppress variable declarations — they may be used later.
    /// </summary>
    static bool IsTrivialRawStatement(string sourceText)
    {
        var text = sourceText.Trim().TrimEnd(';');

        // Variable declarations are NOT trivial — the variable may be used later.
        if (text.StartsWith("var ", StringComparison.Ordinal))
            return false;

        // Standalone property access on known read-only patterns — no assignment, no side effect.
        // These are visibility/text checks that returned a value not used elsewhere in this statement.
        if (text.EndsWith(".Visible.Get()", StringComparison.Ordinal))
            return true;
        if (text.EndsWith(".Text.Get()", StringComparison.Ordinal))
            return true;

        return false;
    }

    void RenderLocalDeclaration(StringBuilder sb, LocalDeclarationAction action)
    {
        RegisterSourceVar(action.VariableName, action.VariableName);
        sb.AppendLine($"{_indent}{_indent}{action.VariableType} {action.VariableName} = {action.InitializationValue}; // line {action.SourceLine}");
    }

    string ExtractKeyName(string keyExpression)
    {
        var expr = keyExpression.Trim();
        if (expr.StartsWith("Keys.", System.StringComparison.Ordinal))
            return expr.Substring("Keys.".Length);
        if (expr.StartsWith('"') && expr.EndsWith('"'))
            return expr.Substring(1, expr.Length - 2);
        return expr;
    }

    string NormalizeAttribute(string rawAttribute)
    {
        var raw = rawAttribute.Trim();
        if (raw.StartsWith("[") && raw.EndsWith("]"))
            return raw;

        if (raw.StartsWith("["))
            return raw + "]";

        return $"[{raw}]";
    }

    string RenderTargetExpression(TargetExpression target)
    {
        if (target is MappedTarget mapped && mapped.TestIdAttribute != null && target.Kind == TargetKind.PlaywrightLocator)
        {
            var attr = EscapeAttribute(mapped.TestIdAttribute);
            var value = EscapeString(mapped.TargetExpression);
            var expr = $"Page.Locator(\"[{attr}='{value}']\")";
            return ApplyMatchStrategy(expr, mapped);
        }

        var rendered = target.RenderLocator();
        return target.Kind switch
        {
            TargetKind.PlaywrightLocator => RenderPlaywrightLocator(mapped: target as MappedTarget, rendered),
            TargetKind.Text => RenderTextLocator(mapped: target as MappedTarget),
            TargetKind.PageObjectProperty => rendered,
            TargetKind.RawExpression => rendered,
            _ => $"Page.Locator(\"TODO: {target.SourceExpression}\")"
        };
    }

    string RenderPlaywrightLocator(MappedTarget? mapped, string rendered)
    {
        if (rendered.StartsWith("Page."))
            return ApplyMatchStrategy(rendered, mapped);

        // Legacy: TargetExpression already contains Playwright call like GetByTestId("x")
        if (IsLegacyPlaywrightFragment(rendered))
        {
            var expr = $"Page.{rendered}";
            return ApplyMatchStrategy(expr, mapped);
        }

        // Semantic: TargetExpression is a raw test-id value like "submit"
        if (mapped != null)
        {
            var expr = $"Page.GetByTestId(\"{mapped.TargetExpression}\")";
            return ApplyMatchStrategy(expr, mapped);
        }

        return $"Page.{rendered}";
    }

    string RenderTextLocator(MappedTarget? mapped)
    {
        if (mapped == null)
            return "Page.GetByText(\"TODO\")";

        var expr = $"Page.GetByText(\"{EscapeString(mapped.TargetExpression)}\")";
        return ApplyMatchStrategy(expr, mapped);
    }

    string ApplyMatchStrategy(string locatorExpr, MappedTarget? mapped)
    {
        if (mapped == null || string.IsNullOrEmpty(mapped.Match))
            return locatorExpr;

        return mapped.Match switch
        {
            "First" => $"{locatorExpr}.First",
            "Nth" => $"{locatorExpr}.Nth({mapped.NthIndex ?? 0})",
            _ => locatorExpr
        };
    }

    static bool IsLegacyPlaywrightFragment(string expr)
    {
        var trimmed = expr.Trim();
        return trimmed.StartsWith("GetByTestId(") ||
               trimmed.StartsWith("Locator(") ||
               trimmed.StartsWith("GetByText(") ||
               trimmed.StartsWith("GetByRole(") ||
               trimmed.StartsWith("GetByLabel(") ||
               trimmed.StartsWith("GetByPlaceholder(") ||
               trimmed.StartsWith("GetByAltText(");
    }

    string EscapeAttribute(string attr)
    {
        return attr.Replace("\\", "\\\\").Replace("\"", "\\\"");
    }

    string EscapeString(string value)
    {
        return value.Replace("\\", "\\\\").Replace("\"", "\\\"").Replace("'", "&#39;");
    }

    string ConvertExpression(string expr)
    {
        if (expr.StartsWith('"') && expr.EndsWith('"'))
            return expr;

        var trimmed = expr.Trim();
        if (_sourceVarMap.TryGetValue(trimmed, out var mapped))
            return mapped;

        return expr;
    }

    string ConvertConstraint(string constraint)
    {
        return constraint;
    }

    static string EscapeComment(string? text)
    {
        if (string.IsNullOrWhiteSpace(text))
            return string.Empty;

        return text
            .Replace("\r\n", " ")
            .Replace("\n", " ")
            .Replace("\r", " ")
            .Replace("\t", " ")
            .Replace("//", "/_/")
            .Trim();
    }

    static string EscapeStringLiteral(string? value)
    {
        if (string.IsNullOrEmpty(value))
            return string.Empty;

        return value
            .Replace("\\", "\\\\")
            .Replace("\"", "\\\"")
            .Replace("\r\n", " ")
            .Replace("\n", " ")
            .Replace("\r", " ");
    }

    string ExpectCall() => _useAssertionsExpect ? "Assertions.Expect" : "Expect";

    void RenderTableRowTextAccess(StringBuilder sb, TableRowTextAccessAction action)
    {
        var target = action.Target;
        var indexExpr = action.IndexExpression;
        var locator = RenderTableRowLocator(target, indexExpr);

        if (target.Kind == TargetKind.Unresolved)
        {
            var escapedComment = EscapeComment(action.SourceText);
            var escapedLocator = EscapeStringLiteral(action.SourceText);
            sb.AppendLine($"{_indent}{_indent}// TODO: table row text access — map source expression: {escapedComment}");
            sb.AppendLine($"{_indent}{_indent}// var {NextTempVar("rowText")} = await Page.Locator(\"TODO: {escapedLocator}\").TextContentAsync();");
        }
        else
        {
            var varName = NextTempVar("rowText");
            var originalVarName = ExtractVariableName(action.SourceText);
            if (!string.IsNullOrEmpty(originalVarName))
            {
                RegisterSourceVar(originalVarName, varName);
            }
            sb.AppendLine($"{_indent}{_indent}var {varName} = await {locator}.TextContentAsync(); // line {action.SourceLine}");
        }
    }

    void ResetMethodScope()
    {
        _tempVarCounter = 0;
        _methodScopeVars.Clear();
        _sourceVarMap.Clear();
    }

    void RegisterSourceVar(string originalName, string generatedName)
    {
        originalName = originalName.Trim();
        if (!string.IsNullOrEmpty(originalName))
        {
            _sourceVarMap[originalName] = generatedName;
        }
    }

    string? ExtractVariableName(string sourceText)
    {
        var match = System.Text.RegularExpressions.Regex.Match(sourceText, @"^\s*var\s+(\w+)\s*=");
        if (match.Success) return match.Groups[1].Value;
        match = System.Text.RegularExpressions.Regex.Match(sourceText, @"^\s*\w+\s+(\w+)\s*=");
        if (match.Success) return match.Groups[1].Value;
        return null;
    }

    void RenderTableCountAssertion(StringBuilder sb, TableCountAssertionAction action)
    {
        var target = action.Target;
        var locator = target.Kind != TargetKind.Unresolved
            ? RenderTargetExpression(target)
            : $"Page.Locator(\"TODO: {EscapeStringLiteral(target.SourceExpression)}\")";

        var expectCall = ExpectCall();
        var countExpr = action.ExpectedCount ?? "0";

        switch (action.Kind)
        {
            case TableCountKind.CountEquals:
                sb.AppendLine($"{_indent}{_indent}await {expectCall}({locator}).ToHaveCountAsync({countExpr}); // line {action.SourceLine}");
                break;
            case TableCountKind.CountGreaterThanZero:
                var nv = NextTempVar("tableCount");
                sb.AppendLine($"{_indent}{_indent}var {nv} = await {locator}.CountAsync(); // line {action.SourceLine}");
                sb.AppendLine($"{_indent}{_indent}Assert.That({nv}, Is.GreaterThan(0));");
                break;
            case TableCountKind.CountLessThanOne:
                var nv2 = NextTempVar("tableCount");
                sb.AppendLine($"{_indent}{_indent}var {nv2} = await {locator}.CountAsync(); // line {action.SourceLine}");
                sb.AppendLine($"{_indent}{_indent}Assert.That({nv2}, Is.LessThan(1));");
                break;
            case TableCountKind.CountGreaterThanOrEqualTo:
                var nv3 = NextTempVar("tableCount");
                sb.AppendLine($"{_indent}{_indent}var {nv3} = await {locator}.CountAsync(); // line {action.SourceLine}");
                sb.AppendLine($"{_indent}{_indent}Assert.That({nv3}, Is.GreaterThanOrEqualTo({countExpr}));");
                break;
            default:
                sb.AppendLine($"{_indent}{_indent}// TODO: table count assertion — {action.Kind}");
                sb.AppendLine($"{_indent}{_indent}//   {EscapeComment(action.SourceText)}");
                break;
        }

        if (target.Kind == TargetKind.Unresolved)
        {
            sb.AppendLine($"{_indent}{_indent}// TODO: map table row target: {EscapeComment(target.SourceExpression)}");
        }
    }

    void RenderTableRowAccess(StringBuilder sb, TableRowAccessAction action)
    {
        // This action represents a table row access that wasn't resolved to a specific operation
        // (click, text, etc). Render as TODO for manual review.
        var target = action.Target;
        var locator = RenderTableRowLocator(target, action.IndexExpression);

        if (target.Kind == TargetKind.Unresolved)
        {
            sb.AppendLine($"{_indent}{_indent}// TODO: table row access — map source expression: {EscapeComment(action.SourceText)}");
            sb.AppendLine($"{_indent}{_indent}// var {NextTempVar("row")} = Page.Locator(\"TODO: {action.SourceText}\").Nth({action.IndexExpression});");
        }
        else
        {
            var varName = NextTempVar("row");
            sb.AppendLine($"{_indent}{_indent}var {varName} = {locator}; // line {action.SourceLine}");
        }
    }

    string RenderTableRowLocator(TargetExpression target, string indexExpr)
    {
        var rowIdx = 0;
        var hasIndex = int.TryParse(indexExpr, out rowIdx);

        if (target is MappedTarget mapped)
        {
            var locator = RenderTargetExpression(target);
            if (hasIndex)
            {
                if (string.IsNullOrEmpty(mapped.Match) || mapped.Match != "Nth")
                {
                    locator += $".Nth({rowIdx})";
                }
            }
            return locator;
        }

        if (hasIndex)
            return $"Page.Locator(\"TODO: {target.SourceExpression}\").Nth({rowIdx})";

        return $"Page.Locator(\"TODO: {target.SourceExpression}\")";
    }
